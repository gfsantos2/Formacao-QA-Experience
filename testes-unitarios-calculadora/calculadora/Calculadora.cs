namespace calculadora;
public class Calculadora
{
    public int Somar(int operador1, int operador2) => operador1 + operador2;
    public int Subtrair(int operador1, int operador2) => operador1 - operador2;
    public int Multiplicar(int operador1, int operador2) => operador1 * operador2;
    public int Dividir(int dividento, int divisor) => dividento / divisor;
}

